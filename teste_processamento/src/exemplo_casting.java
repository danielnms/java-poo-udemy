
public class exemplo_casting {

	public static void main(String[] args) {
		// Casting: Conversão explícita de valores
		
	int a, b;
	double resultado;
	
	a = 5;
	b = 2;
	
		// A expressão "(double)" converte o resultado de "int" para "double"
	resultado = (double) 5 / 2;
	
	System.out.println (resultado);

	}

}
